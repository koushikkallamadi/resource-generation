def extract_pdf_text(pdf_path: Path, max_chars: int = 12000) -> str:
    text = ""
    try:
        with pdfplumber.open(pdf_path) as pdf:
            for page in pdf.pages:
                text += (page.extract_text() or "") + "\n"
                if len(text) > max_chars:
                    break
    except Exception as e:
        print(f"  [WARN] PDF read error {pdf_path.name}: {e}")
    return text[:max_chars].strip()


def extract_docx_text(docx_path: Path, max_chars: int = 12000) -> str:
    """
    Extract text from DOCX file.
    Return plain text similar to extract_pdf_text().
    """
    text = ""
    try:
        doc = Document(docx_path)
        for para in doc.paragraphs:
            text += (para.text or "") + "\n"
            if len(text) > max_chars:
                break
    except Exception as e:
        print(f"  [WARN] DOCX read error {docx_path.name}: {e}")
    return text[:max_chars].strip()


def get_model_candidates() -> list[str]:
    return ["gemini-flash-lite-latest"]


def call_gemini(contents, retries: int = 5) -> str:
    models = get_model_candidates()
    for model in models:
        for attempt in range(1, retries + 1):
            try:
                resp = client.models.generate_content(model=model, contents=contents)
                text = resp.text.strip()
                if text:
                    return text
                raise ValueError("Empty response from Gemini")
            except Exception as e:
                wait = 65
                print(
                    f"\n  [WARN] Gemini ({model}) error ({e}), retry {attempt}/{retries} in {wait}s …",
                    end="",
                    flush=True,
                )
                time.sleep(wait)
        print(f"\n  [WARN] Model {model} failed, switching to next available model.")
    return "[ERROR: Gemini API failed]"


def save_json(data, path: Path):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")


def save_md(text: str, path: Path):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def load_metadata(path: Path) -> dict:
    if path.exists():
        try:
            return json.loads(path.read_text(encoding="utf-8"))
        except Exception:
            pass
    return {}


