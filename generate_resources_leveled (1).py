# ── Per-chapter processing ─────────────────────────────────────────────────


def process_chapter_level(
    subject_key: str,
    ch_num: int,
    ch_name: str,
    text: str,
    ch_dir: Path,
    level: str,
    meta: dict,
    dry_run: bool,
) -> dict:
    """Generate all 8 resources for a single chapter + level combination."""
    subj = subject_key.capitalize()
    level_dir = ch_dir / level
    level_dir.mkdir(parents=True, exist_ok=True)

    done = meta.get("completed", {}).get(level, [])

    for res_name, (prompt_fn, ext) in LEVEL_PROMPTS[level].items():
        if res_name in done:
            print(f"      [OK] {res_name} ({level}, cached)")
            continue

        tag = f"[{level.upper()}] {res_name}"
        if dry_run:
            print(f"      [--] Would generate: {tag}")
            done.append(res_name)
            continue

        print(f"      [..] {tag} ...", end=" ", flush=True)

        prompt_str = prompt_fn(subj, ch_name, text)
        if subject_key.lower() == "physics":
            if ext == ".json":
                prompt_str += "\n\nCRUCIAL PHYSICS FORMATTING: Use proper LaTeX for any math equations ($E=mc^2$). Ensure the output remains perfectly valid JSON without any markdown formatting blocks."
            else:
                prompt_str += "\n\nCRUCIAL PHYSICS FORMATTING:\n- Use proper LaTeX formatting for mathematical equations ($$E=mc^2$$ for block math, $F=ma$ for inline math).\n- Use standard Markdown tables for data, comparisons, and formulas.\n- Use Mermaid.js syntax (```mermaid) for flowcharts or system diagrams if helpful.\n- Emphasize derivations, conceptual physics reasoning, numerical properties, and physical constants."

        contents_payload = [text, prompt_str] if text else prompt_str
        result = call_gemini(contents_payload)
        time.sleep(RATE_LIMIT_DELAY)

        # --- Podcast Expansion Logic ---
        if res_name in ["short_podcast", "long_podcast"]:
            min_words = 800 if res_name == "short_podcast" else 2200

            while True:
                word_count = len(result.split())
                est_duration = word_count / 150.0

                print(
                    f"\n      [LOG] {res_name}: {word_count} words, ~{est_duration:.1f} mins",
                    end=" ",
                    flush=True,
                )

                if word_count >= min_words:
                    print("- Meets duration requirement.")
                    break

                print(
                    f"- Expanding (target > {min_words} words)...", end=" ", flush=True
                )

                context = result[-1000:]

                expand_prompt = f"""You are continuing a {res_name.replace('_', ' ')}. 
The script so far is {word_count} words long, but it needs to be at least {min_words} words to reach the minimum duration requirement.
Please CONTINUE the script naturally from exactly where it left off. Do NOT write a new introduction or repeat what was already said.
Continue the engaging discussion between Boy and Girl, adding more examples, analogies, and back-and-forth dialogue.

Ensure the format strictly follows:
Host 1: [dialogue]
Host 2: [dialogue]

Previous script ending for context:
{context}"""

                expansion = call_gemini(expand_prompt)
                result += "\n\n" + expansion.strip()
                time.sleep(RATE_LIMIT_DELAY)
        # --- End Podcast Expansion Logic ---

        out_path = level_dir / (res_name + ext)

        if ext == ".json":
            raw = re.sub(
                r"```(?:json)?\s*|\s*```", "", result, flags=re.IGNORECASE
            ).strip()
            try:
                data = json.loads(raw)
                save_json(
                    {
                        "chapter": ch_name,
                        "subject": subj,
                        "level": level,
                        "flashcards": data,
                    },
                    out_path,
                )
            except json.JSONDecodeError:
                # Fall back to saving as markdown if JSON fails
                save_md(result, out_path.with_suffix(".md"))
        else:
            save_md(result, out_path)

        done.append(res_name)
        print("[OK]")

    # Update metadata
    if "completed" not in meta:
        meta["completed"] = {}
    meta["completed"][level] = done
    return meta


def process_chapter(
    subject_key: str,
    ch_index: int,
    pdf_path: Path,
    ch_name: str,
    out_dir: Path,
    levels_to_run: list,
    dry_run: bool,
):
    ch_num = ch_index + 1
    ch_dir = out_dir / f"chapter_{ch_num:02d}"
    ch_dir.mkdir(parents=True, exist_ok=True)

    meta_path = ch_dir / "metadata_leveled.json"
    meta = load_metadata(meta_path)
    if not meta:
        meta = {
            "chapter_number": ch_num,
            "chapter_name": ch_name,
            "subject": subject_key.capitalize(),
            "pdf": pdf_path.name,
            "completed": {},
        }

    subj = subject_key.capitalize()
    print(f"\n  [>>] [{subj}] Ch {ch_num}: {ch_name}")

    if dry_run:
        text = ""
    else:
        suffix = pdf_path.suffix.lower()
        if suffix == ".pdf":
            print(
                f"  [UPLOAD] Uploading {pdf_path.name} to Gemini API for native multimodal processing..."
            )
            try:
                text = client.files.upload(file=str(pdf_path))
                print(
                    f"  [UPLOAD] Waiting for document to process...", end="", flush=True
                )
                while text.state.name == "PROCESSING":
                    time.sleep(2)
                    print(".", end="", flush=True)
                    text = client.files.get(name=text.name)
                if text.state.name == "FAILED":
                    print(f"\n  [ERROR] Document processing failed on Gemini server.")
                    return
                print(" [READY]")
            except Exception as e:
                print(f"\n  [ERROR] Upload failed: {e}")
                return
        elif suffix == ".docx":
            text = extract_docx_text(pdf_path)
        else:
            print(f"  [WARN] Unsupported file type: {suffix}")
            return

        if not text:
            print("     [SKIP] No document content found.")
            return

    # Generate Shared Mindmap
    mindmap_path = ch_dir / "mindmap.json"
    mindmap_md_path = ch_dir / "mindmap.md"
    if mindmap_path.exists() or mindmap_md_path.exists():
        print(f"    [SHARED] mindmap (cached)")
    elif dry_run:
        print(f"    [SHARED] Would generate: mindmap")
    else:
        print(f"    [SHARED] Generating mindmap ...", end=" ", flush=True)
        mm_prompt = p_mindmap(subj, ch_name, text)
        mm_payload = [text, mm_prompt] if text else mm_prompt
        mm_result = call_gemini(mm_payload)
        time.sleep(RATE_LIMIT_DELAY)

        raw_mm = re.sub(
            r"```(?:json)?\s*|\s*```", "", mm_result, flags=re.IGNORECASE
        ).strip()
        try:
            mm_data = json.loads(raw_mm)
            save_json(mm_data, mindmap_path)
            print("[OK]")
        except json.JSONDecodeError:
            save_md(mm_result, mindmap_md_path)
            print("[WARN: Invalid JSON, saved as .md]")

    for level in levels_to_run:
        print(f"    [*] Level: {level.upper()}")
        meta = process_chapter_level(
            subject_key, ch_num, ch_name, text, ch_dir, level, meta, dry_run
        )
        if not dry_run:
            save_json(meta, meta_path)

    print(f"  [DONE] Ch {ch_num} done for levels: {', '.join(levels_to_run)}")


# ── README generator ───────────────────────────────────────────────────────

SHARED_RESOURCES = [
    "mindmap",
    "learning_path",
    "podcast_script",
    "youtube_links",
    "course_offerings",
]

LEVELED_RESOURCES = [
    "question_bank",
    "detailed_view",
    "summary",
    "flashcards",
    "quiz",
    "short_podcast",
    "long_podcast",
    "video_script",
]


def write_index(out_dir: Path, subject: str, chapters: list):
    lines = [f"# NCERT Class 10 {subject.capitalize()} – Study Resources\n\n"]

    for i, ch in enumerate(chapters, 1):
        ch_folder = f"chapter_{i:02d}"
        lines.append(f"## Chapter {i}: {ch}\n\n")

        lines.append("### Shared Resources\n")
        for res in SHARED_RESOURCES:
            # Try .md and .json
            for ext in [".md", ".json"]:
                f = f"{ch_folder}/{res}{ext}"
                if (out_dir / ch_folder / (res + ext)).exists():
                    lines.append(f"- [{res.replace('_',' ').title()}]({f})\n")
                    break

        lines.append("\n### Leveled Resources\n")
        lines.append("| Resource | Easy | Moderate | Expert |\n")
        lines.append("|---|---|---|---|\n")
        for res in LEVELED_RESOURCES:
            ext = ".json" if res == "flashcards" else ".md"
            row = f"| {res.replace('_',' ').title()} |"
            for level in LEVELS:
                path = out_dir / ch_folder / level / (res + ext)
                if path.exists():
                    row += f" [✅]({ch_folder}/{level}/{res}{ext}) |"
                else:
                    row += " ❌ |"
            lines.append(row + "\n")
        lines.append("\n")

    lines.append("## [PYQ Analysis](pyq_analysis/pyq_analysis.md)\n")
    save_md("".join(lines), out_dir / "README.md")
    print(f"  [README] Updated: {out_dir / 'README.md'}")


# ── Main ───────────────────────────────────────────────────────────────────


def parse_args():
    p = argparse.ArgumentParser(description="NCERT Leveled Resource Generator")
    p.add_argument("--dry-run", action="store_true", help="Preview without API calls")
    p.add_argument("--subject", choices=list(SUBJECTS.keys()), help="Only one subject")
    p.add_argument("--chapter", type=int, help="Only this chapter number (1-based)")
    p.add_argument("--level", choices=LEVELS, help="Only this difficulty level")
    return p.parse_args()


def main():
    args = parse_args()

    subjects_to_run = [args.subject] if args.subject else list(SUBJECTS.keys())
    levels_to_run = [args.level] if args.level else LEVELS

    print("=" * 65)
    print("  NCERT Class X | Leveled Resource Generator | Gemini 2.5 Flash")
    print("=" * 65)
    if args.dry_run:
        print("  [DRY-RUN MODE] - no API calls will be made\n")
    print(f"  Subjects : {', '.join(subjects_to_run)}")
    print(f"  Levels   : {', '.join(levels_to_run)}")
    print(f"  Output   : {OUTPUT_ROOT}\n")

    for subject_key in subjects_to_run:
        cfg = SUBJECTS[subject_key]
        out_dir = OUTPUT_ROOT / subject_key

        print(f"\n{'='*65}")
        print(f"  [{subject_key.upper()}]  ({len(cfg['chapters'])} chapters)")
        print(f"{'='*65}")

        for idx, (pdf_name, ch_name) in enumerate(
            zip(cfg["chapters"], cfg["chapter_names"])
        ):
            ch_num = idx + 1
            if args.chapter and ch_num != args.chapter:
                continue

            pdf_path = cfg["folder"] / pdf_name
            if not pdf_path.exists():
                print(f"  [MISSING] {pdf_path}")
                continue

            process_chapter(
                subject_key,
                idx,
                pdf_path,
                ch_name,
                out_dir,
                levels_to_run,
                args.dry_run,
            )

        # Regenerate README with leveled links
        write_index(out_dir, subject_key, cfg["chapter_names"])

    print("\n" + "=" * 65)
    if args.dry_run:
        print("  [OK] DRY-RUN COMPLETE - no files were changed.")
    else:
        print("  [OK] ALL LEVELED RESOURCES GENERATED!")
    print(f"  Output folder: {OUTPUT_ROOT}")
    print("=" * 65)


if __name__ == "__main__":
    main()
